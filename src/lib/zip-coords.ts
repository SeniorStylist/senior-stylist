function haversineMiles(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 3958.8
  const dLat = ((lat2 - lat1) * Math.PI) / 180
  const dLng = ((lng2 - lng1) * Math.PI) / 180
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLng / 2) ** 2
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a))
}

/**
 * Returns all ZIP codes in the lookup table within radiusMiles of the given ZIP.
 * Returns [] if the ZIP is not in the table.
 */
export function getZipsWithinMiles(zipCode: string, radiusMiles: number): string[] {
  const origin = ZIP_COORDS[zipCode]
  if (!origin) return []
  return Object.entries(ZIP_COORDS)
    .filter(([, c]) => haversineMiles(origin.lat, origin.lng, c.lat, c.lng) <= radiusMiles)
    .map(([zip]) => zip)
}

/**
 * Extracts the first 5-digit ZIP code found in a location string.
 * Returns null if no ZIP is found.
 */
export function extractZip(location: string): string | null {
  const m = location.match(/\b(\d{5})\b/)
  return m ? m[1] : null
}
